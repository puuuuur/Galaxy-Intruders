package com.hamiko.galaxyintruder.physics;

public class Position {

    private int x;
    private int y;

    public int getX() {
        return x;
    }

    public Position setX(int x) {
        this.x = x;
        return this;
    }

    public int getY() {
        return y;
    }

    public Position setY(int y) {
        this.y = y;
        return this;
    }

}
